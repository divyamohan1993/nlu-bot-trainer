export default function Sidebar() {
  const pathname = usePathname();
  const navRef = useRef<HTMLElement>(null);

  // Keyboard shortcuts: Alt+1 through Alt+5
  const handleKeydown = useCallback((e: KeyboardEvent) => {
    if (!e.altKey) return;
    const item = NAV_ITEMS.find((n) => n.shortcut === e.key);
    if (item) {
      e.preventDefault();
      window.location.href = item.href;
    }
  }, []);

  useEffect(() => {
    window.addEventListener("keydown", handleKeydown);
    return () => window.removeEventListener("keydown", handleKeydown);
  }, [handleKeydown]);

  // Arrow key navigation within nav
  const handleNavKeydown = (e: React.KeyboardEvent, idx: number) => {
    const links = navRef.current?.querySelectorAll("a");
    if (!links) return;
    if (e.key === "ArrowDown") {
      e.preventDefault();
      const next = (idx + 1) % links.length;
      (links[next] as HTMLElement).focus();
    } else if (e.key === "ArrowUp") {
      e.preventDefault();
      const prev = (idx - 1 + links.length) % links.length;
      (links[prev] as HTMLElement).focus();
    }
  };

  return (
    <aside
      className="w-64 h-screen fixed left-0 top-0 bg-surface-1 border-r border-white/5 flex flex-col z-50"
      aria-label="Main navigation"
    >
      {/* Logo */}
      <div className="p-6 border-b border-white/5">
        <div className="flex items-center gap-3">
          <div className="w-9 h-9 rounded-xl bg-gradient-to-br from-brand-500 to-purple-500 flex items-center justify-center" aria-hidden="true">
            <svg className="w-5 h-5 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M9.75 3.104v5.714a2.25 2.25 0 01-.659 1.591L5 14.5M9.75 3.104c-.251.023-.501.05-.75.082m.75-.082a24.301 24.301 0 014.5 0m0 0v5.714c0 .597.237 1.17.659 1.591L19 14.5M14.25 3.104c.251.023.501.05.75.082M19 14.5l-2.47 2.47a2.25 2.25 0 00-.659 1.591v1.189a.75.75 0 01-.75.75h-6.244a.75.75 0 01-.75-.75v-1.19a2.25 2.25 0 00-.659-1.59L5 14.5m14 0h.25a.75.75 0 01.75.75v1a.75.75 0 01-.75.75h-.25m-14 0H4.75a.75.75 0 01-.75-.75v-1a.75.75 0 01.75-.75H5" />
            </svg>
          </div>
          <div>
            <h1 className="font-bold text-sm text-white tracking-tight">NLU Bot Trainer</h1>
            <p className="text-[10px] text-gray-500 tracking-wider uppercase">E-Commerce Support</p>
          </div>
        </div>
      </div>

      {/* Navigation */}
      <nav ref={navRef} className="flex-1 p-3 space-y-1" aria-label="Primary">
        {NAV_ITEMS.map((item, idx) => {
          const isActive = pathname === item.href;
          return (
            <Link
              key={item.href}
              href={item.href}
              aria-current={isActive ? "page" : undefined}
              onKeyDown={(e) => handleNavKeydown(e, idx)}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all duration-200 ${
                isActive
                  ? "bg-brand-600/15 text-brand-400 shadow-sm"
                  : "text-gray-400 hover:text-gray-200 hover:bg-white/5"
              }`}
            >
              <span className={isActive ? "text-brand-400" : "text-gray-500"}>
                {item.icon}
              </span>
              {item.label}
              <span className="ml-auto text-[10px] text-gray-600 font-mono" aria-label={`Alt+${item.shortcut}`}>
                {`Alt+${item.shortcut}`}
              </span>
            </Link>
          );
        })}
      </nav>

      {/* Footer */}
      <div className="p-4 border-t border-white/5 space-y-2">
        <p className="text-[10px] text-gray-600 text-center">
          Ensemble v3: LR + CNB + SVM + MLP + GBM (171K params)
        </p>
        <p className="text-[10px] text-gray-700 text-center">
          Keyboard: Alt+1-9 to navigate
        </p>
      </div>
    </aside>
  );
}
